public class Plants {
    private String pavadinimas;
    private String lotyniskasPavadinimas;
    private boolean vienmetis;
    private String augaZemyne;
    private double augaloAukstis;
    private boolean valgomas;
    public Plants() {}
    public String getPavadinimas() {
        return pavadinimas;
    }
    public void setPavadinimas(String pavadinimas) {
        this.pavadinimas = pavadinimas;
    }
    public String getLotyniskasPavadinimas() {
        return lotyniskasPavadinimas;
    }
    public void setLotyniskasPavadinimas(String lotyniskasPavadinimas) {
        this.lotyniskasPavadinimas = lotyniskasPavadinimas;
    }
    public boolean isVienmetis() {
        return vienmetis;
    }
    public void setVienmetis(boolean vienmetis) {
        this.vienmetis = vienmetis;
    }
    public String getAugaZemyne() {
        return augaZemyne;
    }
    public void setAugaZemyne(String augaZemyne) {
        this.augaZemyne = augaZemyne;
    }
    public double getAugaloAukstis() {
        return augaloAukstis;
    }
    public void setAugaloAukstis(double augaloAukstis) {
        this.augaloAukstis = augaloAukstis;
    }
    public boolean isValgomas() {
        return valgomas;
    }
    public void setValgomas(boolean valgomas) {
        this.valgomas = valgomas;
    }
}